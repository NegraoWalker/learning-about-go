	var array3 [4]string //1°Forma de declaração
	fmt.Println("Array 3: ", array3)
	fmt.Println("===================================================================================================================")
	/*===================================================================================================================*/
	array1[0] = 0
	array1[1] = 1
	fmt.Println("Array 1: ", array1)
	array3[0] = "TV"
	array3[2] = "Cama"
	fmt.Println("Array 3: ", array3)
	fmt.Println("===================================================================================================================")
